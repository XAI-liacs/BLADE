"""Toy bi-objective benchmark for testing multi-objective support in BLADE.

The problem asks the LLM to generate a **search algorithm** that simultaneously
minimises two conflicting objectives on the bi-sphere function:

.. math::

    f_1(x) = x^2
    f_2(x) = (x - 2)^2

The Pareto front lies on :math:`x \\in [0, 2]`:

* Perfect minimiser of :math:`f_1` â†’ :math:`x = 0`  (:math:`f_1=0, f_2=4`)
* Perfect minimiser of :math:`f_2` â†’ :math:`x = 2`  (:math:`f_1=4, f_2=0`)

The algorithm generated by the LLM must implement::

    def run(budget: int = 100) -> tuple[float, float]:
        \"\"\"Return (best_f1, best_f2) found within `budget` evaluations.\"\"\"

A reference solution is shown in :attr:`ToyMultiObjective.example_prompt`.
"""

from __future__ import annotations

import inspect
import textwrap

from typing import Any

from iohblade.fitness import Fitness
from iohblade.problem import Problem
from iohblade.solution import Solution

_REFERENCE_CODE = textwrap.dedent("""\
    import numpy as np

    class BiSphereSearcher:
        \"\"\"Reference searcher for the toy bi-objective benchmark.

        Uniformly samples `budget` points in [-1, 3] and returns the
        best values found for each objective independently.
        \"\"\"

        def run(self, budget: int = 100) -> tuple[float, float]:
            rng = np.random.default_rng(42)
            xs = rng.uniform(-1.0, 3.0, size=budget)
            f1s = xs ** 2
            f2s = (xs - 2.0) ** 2
            return float(f1s.min()), float(f2s.min())
    """)


class ToyMultiObjective(Problem):
    """Toy bi-objective problem for demonstrating multi-objective support.

    The generated algorithm should search for a scalar :math:`x \\in [-1, 3]`
    that simultaneously minimises:

    * **f1** = :math:`x^2`           (distance to 0, in [0, 9])
    * **f2** = :math:`(x - 2)^2`     (distance to 2, in [0, 9])

    Both objectives conflict: a perfect solution for *f1* (*x* = 0) is the
    worst possible for *f2* and vice-versa.

    The returned :class:`~iohblade.fitness.Fitness` uses **negated** values so
    that larger is better, which is consistent with BLADE's maximisation
    convention:

    * ``fitness["f1"]`` = âˆ’best_f1  (maximise â†’ minimise *f1*)
    * ``fitness["f2"]`` = âˆ’best_f2  (maximise â†’ minimise *f2*)

    Parameters
    ----------
    budget:
        Number of function evaluations the generated algorithm may use.
    name:
        Problem name (shown in logs and plots).
    """

    task_prompt = textwrap.dedent("""\
        You are given a bi-objective optimisation task. Your goal is to write
        a Python class that searches for a scalar value x in [-1, 3] and
        simultaneously minimises:

            f1(x) = x ** 2
            f2(x) = (x - 2) ** 2

        These objectives conflict: the optimal x for f1 is 0, while the
        optimal x for f2 is 2. You should try to find a good compromise.

        Your class must be named `BiSphereSearcher` and implement the
        following interface:

            def run(self, budget: int = 100) -> tuple[float, float]:
                ...

        The method must return `(best_f1, best_f2)` â€” the best (lowest)
        values for each objective found within `budget` function evaluations.
        You are free to use any search strategy you like (random search,
        grid search, gradient-free optimisation, etc.).
        """)

    example_prompt = textwrap.dedent(f"""\
        Here is a simple reference implementation:

        ```python
        {_REFERENCE_CODE}
        ```
        """)

    format_prompt = textwrap.dedent("""\
        Your solution must be a Python class named `BiSphereSearcher` with a
        `run(self, budget: int = 100) -> tuple[float, float]` method.
        Wrap the class in a Python code block:

        ```python
        class BiSphereSearcher:
            ...
        ```
        """)

    def __init__(self, budget: int = 100, name: str = "ToyMultiObjective"):
        super().__init__(name=name)
        self.eval_budget = budget

    def get_prompt(self) -> str:  # pragma: no cover
        return (
            ToyMultiObjective.task_prompt
            + "\n\n"
            + ToyMultiObjective.example_prompt
            + "\n\n"
            + ToyMultiObjective.format_prompt
        )

    def evaluate(self, solution: Solution) -> Solution:
        """Execute the generated algorithm and score it with a Fitness object.

        Parameters
        ----------
        solution:
            A :class:`~iohblade.solution.Solution` whose ``code`` attribute
            must define a class ``BiSphereSearcher`` with a ``run`` method.

        Returns
        -------
        Solution
            The same solution with ``fitness`` set to a
            :class:`~iohblade.fitness.Fitness` mapping
            ``{"f1": -best_f1, "f2": -best_f2}``.
        """
        namespace: dict = {}
        try:
            exec(solution.code, namespace)  # noqa: S102
        except Exception as exc:
            return solution.set_scores(
                fitness=Fitness({"f1": float("nan"), "f2": float("nan")}),
                feedback="Code failed to execute.",
                error=exc,
            )

        cls = namespace.get("BiSphereSearcher")
        if cls is None:
            return solution.set_scores(
                fitness=Fitness({"f1": float("nan"), "f2": float("nan")}),
                feedback="Class `BiSphereSearcher` not found in the generated code.",
            )

        try:
            searcher = cls()
            result = searcher.run(budget=self.eval_budget)
            best_f1, best_f2 = float(result[0]), float(result[1])
        except Exception as exc:
            return solution.set_scores(
                fitness=Fitness({"f1": float("nan"), "f2": float("nan")}),
                feedback=f"run() raised an exception: {exc}",
                error=exc,
            )

        # BLADE maximises: negate so that closer to 0 â†’ higher fitness
        fitness = Fitness({"f1": -best_f1, "f2": -best_f2})
        feedback = f"Best f1 (xÂ²) = {best_f1:.4f}, best f2 ((x-2)Â²) = {best_f2:.4f}."
        return solution.set_scores(fitness=fitness, feedback=feedback)

    def test(self, solution: Solution) -> Solution:
        """Run evaluate as the test phase (same logic as training)."""
        return self.evaluate(solution)

    def to_dict(self) -> dict:
        return {"name": self.name, "eval_budget": self.eval_budget}

    def get_config(self) -> dict[str, Any]:
        evaluator = inspect.getsource(self.evaluate)

        config = {
            "tags": ["spherical", "multi-objective", "BBOB"],
            "name": "Toy Multi-Objective",
            "prompt": self.get_prompt(),
            "minimisation": False,
            "evaluator": evaluator,
            "config": {
                "budget": self.eval_budget,
                "keys": ["f1", "f2"],
                "depencencies": self.dependencies,
                "imports": self.imports,
            },
        }
        return config


if __name__ == "__main__":
    tm = ToyMultiObjective()
    for key, value in tm.get_config().items():
        print(f"------------------------------{key}------------------------------")
        print(value)
